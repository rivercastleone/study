package day3;

public class Loopapp {
    public static void main(String[] args){
        System.out.println("====WHILE====");
        int i= 0;
        while(i<3){
            System.out.println("Hi");
            System.out.println("HELLO");
        i++;
        }
        System.out.println("===for===");
        int j=0;
        
        for(j=0; j<3; j++){
            System.out.println("ASDF");
            System.out.println("ㅁㄴㅇㄹ");
        }

    }
}
